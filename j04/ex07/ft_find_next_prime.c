int		ft_find_next_prime(int nb)
{
	int		i;

	i = 2;
	if (nb <= 2)
		return (2);
	while (i > 1 && i < nb)
	{
		if ((nb % i) != 0)
			i++;
		else if ((nb % i) == 0 && i < nb)
		{
			nb++;
			i = 2;
		}
	}
	return (nb);
}
